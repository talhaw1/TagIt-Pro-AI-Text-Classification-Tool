from tqdm import tqdm
import json
import os

# Configuration
MODEL_ID = "facebook/bart-large-mnli"
EMBED_MODEL = "all-MiniLM-L6-v2"
CONF_THRESHOLD = 0.6

device = 0 if torch.cuda.is_available() else -1

print("Loading Zero-Shot Model...")
classifier = pipeline("zero-shot-classification", model=MODEL_ID, device=device)

print("Loading Embedding Model...")
embedder = SentenceTransformer(EMBED_MODEL)

print("Setup Complete ✅")

# ---------------- Label Similarity Check ----------------
def check_label_similarity(labels):
    if len(labels) < 2:
        return

    embeddings = embedder.encode(labels, normalize_embeddings=True)
    similarity = np.dot(embeddings, embeddings.T)

    print("\n🔍 Label Similarity Check:")
    for i in range(len(labels)):
        for j in range(i+1, len(labels)):
            if similarity[i][j] > 0.8:
                print(f"⚠ {labels[i]} and {labels[j]} are very similar (score={similarity[i][j]:.2f})")

# ---------------- Single Text Classification ----------------
def classify_text(text, labels, threshold=CONF_THRESHOLD):
    result = classifier(text, labels)

    print("\n📊 Classification Results:")
    for l, s in zip(result['labels'], result['scores']):
        print(f"{l}: {s:.4f}")

    top_label = result['labels'][0]
    top_score = result['scores'][0]

    if top_score < threshold:
        print("\n⚠ Low confidence — Needs Review")
    else:
        print(f"\n✅ Final Prediction: {top_label} ({top_score:.4f})")

# ---------------- Batch Classification ----------------
def batch_classify(file_path, labels, output_file="results.csv", threshold=CONF_THRESHOLD):

    if not os.path.exists(file_path):
        print("File not found!")
        return

    with open(file_path, "r", encoding="utf-8") as f:
        lines = [line.strip() for line in f if line.strip()]

    results = []

    print("\n🚀 Running Batch Classification...")
    for text in tqdm(lines):
        result = classifier(text, labels)
        top_label = result['labels'][0]
        top_score = result['scores'][0]

        results.append({
            "text": text,
            "top_label": top_label,
            "confidence": top_score,
            "needs_review": top_score < threshold,
            "all_scores": json.dumps(dict(zip(result['labels'], result['scores'])))
        })

    df = pd.DataFrame(results)
    df.to_csv(output_file, index=False)

    print(f"\n✅ Batch results saved to {output_file}")

    # Summary Analytics
    print("\n📈 Summary Analytics:")
    print("\nTop Label Counts:")
    print(df["top_label"].value_counts())

    print(f"\nAverage Confidence: {df['confidence'].mean():.4f}")
    print(f"Total Low-Confidence Cases: {df['needs_review'].sum()}")

