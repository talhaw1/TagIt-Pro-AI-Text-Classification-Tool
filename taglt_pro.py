import gradio as gr
from transformers import pipeline
import pandas as pd
import os

# Load zero-shot classification model
classifier = pipeline("zero-shot-classification", model="facebook/bart-large-mnli")

CONF_THRESHOLD = 0.5

# -----------------------------
# Single Text Classification
# -----------------------------
def classify_text(text, labels_input, threshold):
    if not text.strip():
        return "Please enter some text."

    labels = [label.strip() for label in labels_input.split(",") if label.strip()]

    if len(labels) < 2:
        return "Please provide at least two labels separated by commas."

    result = classifier(text, labels)

    output = ""
    for label, score in zip(result["labels"], result["scores"]):
        status = "⚠️ Needs Review" if score < threshold else ""
        output += f"{label} → {score:.4f} {status}\n"

    return output


# -----------------------------
# Batch Classification
# -----------------------------
def batch_classify(file_obj, labels_input, threshold):
    if file_obj is None:
        return "Please upload a .txt file."

    labels = [label.strip() for label in labels_input.split(",") if label.strip()]

    if len(labels) < 2:
        return "Please provide at least two labels separated by commas."

    results = []

    with open(file_obj.name, "r", encoding="utf-8") as f:
        lines = f.readlines()

    for line in lines:
        text = line.strip()
        if text:
            result = classifier(text, labels)
            top_label = result["labels"][0]
            top_score = result["scores"][0]
            review_flag = "Needs Review" if top_score < threshold else "OK"

            results.append([text, top_label, round(top_score, 4), review_flag])

    df = pd.DataFrame(results, columns=["Text", "Predicted Label", "Confidence", "Status"])

    output_file = "batch_results.csv"
    df.to_csv(output_file, index=False)

    return df


# -----------------------------
# Gradio Interface
# -----------------------------
with gr.Blocks() as demo:
    gr.Markdown("# 🧠 TagIt Pro - Zero-Shot Text Classifier")

    with gr.Tab("Single Text Mode"):
        text_input = gr.Textbox(label="Enter Text")
        labels_input = gr.Textbox(label="Enter Labels (comma separated)")
        threshold_slider = gr.Slider(0, 1, value=0.5, label="Confidence Threshold")
        output_box = gr.Textbox(label="Classification Results")
        classify_btn = gr.Button("Classify")

        classify_btn.click(
            classify_text,
            inputs=[text_input, labels_input, threshold_slider],
            outputs=output_box
        )

    with gr.Tab("Batch Mode"):
        file_input = gr.File(label="Upload .txt File")
        batch_labels_input = gr.Textbox(label="Enter Labels (comma separated)")
        batch_threshold_slider = gr.Slider(0, 1, value=0.5, label="Confidence Threshold")
        batch_output = gr.Dataframe(label="Batch Results")
        batch_btn = gr.Button("Run Batch Classification")

        batch_btn.click(
            batch_classify,
            inputs=[file_input, batch_labels_input, batch_threshold_slider],
            outputs=batch_output
        )

demo.launch(share=True)